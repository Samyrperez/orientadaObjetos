// Fetch
// Que es fetch?
// Fetch es una API de JavaScript que permite hacer peticiones HTTP desde el navegador. Es una alternativa moderna a XMLHttpRequest. Es la forma moderna de  peticiones AJAX sin usar XMLHttpRequest.
// Para que sirve fetch?
// Nos permite obtener datos de una api, enviar datos a un servidor, descargar archivos, etc.

// como usar fetch?
// fetch(url)
// Hace una solicitud GET a la URL especificada.

// Ejemplo
// fetch('https://jsonplaceholder.typicode.com/posts')
